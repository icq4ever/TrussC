#!/usr/bin/env node
// =============================================================================
// coverage-inventory.js — human-reviewable markdown of the coverage residual
// =============================================================================
//
// Reads `coverage-audit.js --json` (the undocumented-public-symbol list, now
// carrying each symbol's `sig` = C++ function type) and emits a grouped markdown
// inventory with return type + params, bucketed by a PROPOSED disposition
// (document / exclude-by-rule / palette), so the residual can be triaged by hand
// toward coverage == 0.
//
//   node coverage-inventory.js > docs/tests/coverage-residual.md
//   node coverage-inventory.js --json <cached>   # reuse a saved residual json
//
// Proposed dispositions are HEURISTIC hints, not decisions — the point is a
// reviewable list, not an automatic gate.

'use strict';
const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const argv = process.argv.slice(2);
const cached = (() => { const i = argv.indexOf('--json'); return i >= 0 ? argv[i + 1] : null; })();

let residual;
if (cached) {
    residual = JSON.parse(fs.readFileSync(cached, 'utf8'));
} else {
    const out = execFileSync('node', [path.join(__dirname, 'coverage-audit.js'), '--json'], { maxBuffer: 256 * 1024 * 1024 }).toString();
    residual = JSON.parse(out);
}

// "Vec2 &(const Vec2 &) const" -> { ret:'Vec2 &', params:'const Vec2 &', qual:'const' }
function parseSig(sig) {
    if (!sig) return null;
    const i = sig.indexOf('(');
    if (i < 0) return { ret: sig.trim(), params: '', qual: '' };
    let d = 0, end = -1;
    for (let j = i; j < sig.length; j++) {
        if (sig[j] === '(') d++;
        else if (sig[j] === ')') { d--; if (d === 0) { end = j; break; } }
    }
    return { ret: sig.slice(0, i).trim(), params: sig.slice(i + 1, end).trim(), qual: sig.slice(end + 1).trim() };
}
function declOf(s) {
    const p = parseSig(s.sig);
    if (!p) return s.name;                                 // var / field / type / enum
    return `${p.ret} ${s.name}(${p.params})${p.qual ? ' ' + p.qual : ''}`;
}

const scopeOf = (s) => s.owner || s.ns || '';

// Proposed disposition buckets (heuristic).
function bucket(s) {
    const o = scopeOf(s), n = s.name || '';
    if (/colors/.test(o)) return 'palette';
    if (o === 'RenderContext') return 'exclude:mirror';     // mirrors documented free functions
    if (/bitmapfont/.test(o)) return 'exclude:bitmapfont';  // low-level bitmap font ns
    if (/tcEnumLabels|tcGetLogger|tcSet(Console|File)|tcSetLogFile|tcCloseLogFile|logLevelToString/.test(n)) return 'exclude:infra';
    if (/^(HasTexture|VideoPlayerBase|ShaderVertex|GlyphCell|CameraContext|LogStream)$/.test(o)) return 'exclude:base';
    return 'document';                                       // genuine public API gap (default)
}

const groups = {};                                          // bucket -> owner -> [syms]
for (const s of residual) {
    const b = bucket(s);
    const owner = s.owner || s.ns || '(global)';
    ((groups[b] ??= {})[owner] ??= []).push(s);
}

const BUCKET_TITLE = {
    document: '## Proposed: DOCUMENT — genuine public API gaps',
    'exclude:mirror': '## Proposed: EXCLUDE — RenderContext (mirrors documented free functions)',
    'exclude:base': '## Proposed: EXCLUDE — base / impl types',
    'exclude:infra': '## Proposed: EXCLUDE — logging / enum-reflection infra',
    'exclude:bitmapfont': '## Proposed: EXCLUDE — low-level bitmapfont namespace',
    palette: '## Proposed: DOCUMENT as a palette — color constants (need compact site rendering)',
};
const BUCKET_ORDER = ['document', 'palette', 'exclude:mirror', 'exclude:base', 'exclude:infra', 'exclude:bitmapfont'];

const total = residual.length;
const lines = [];
lines.push(`# API coverage residual — undocumented public symbols`);
lines.push('');
lines.push(`${total} public C++ symbols are in the real surface but not in \`api-definition.yaml\`.`);
lines.push(`Generated by \`coverage-inventory.js\` (from \`coverage-audit.js --json\`). Dispositions are heuristic hints — triage each toward **document** / **exclude (principled rule)** / **palette**.`);
lines.push('');
const counts = BUCKET_ORDER.map((b) => `${b}: ${Object.values(groups[b] || {}).reduce((a, v) => a + v.length, 0)}`).join(' · ');
lines.push(`**Totals:** ${counts}`);
lines.push('');

for (const b of BUCKET_ORDER) {
    const byOwner = groups[b];
    if (!byOwner) continue;
    const bTotal = Object.values(byOwner).reduce((a, v) => a + v.length, 0);
    lines.push(BUCKET_TITLE[b] + ` (${bTotal})`);
    lines.push('');
    const owners = Object.keys(byOwner).sort((a, b) => byOwner[b].length - byOwner[a].length);
    for (const owner of owners) {
        const syms = byOwner[owner];
        lines.push(`### ${owner} (${syms.length})`);
        lines.push('');
        lines.push('| kind | declaration |');
        lines.push('|---|---|');
        for (const s of syms.sort((a, b) => (a.name || '').localeCompare(b.name || ''))) {
            lines.push(`| ${s.kind} | \`${declOf(s).replace(/\|/g, '\\|')}\` |`);
        }
        lines.push('');
    }
}

process.stdout.write(lines.join('\n') + '\n');
